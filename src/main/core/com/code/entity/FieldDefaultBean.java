package com.code.entity;

/**
 * 字段默认值解析
 * @author zzl
 * Date:2014-08-15
 */
public class FieldDefaultBean {
    private String value;
    private String text;

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
